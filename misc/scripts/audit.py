issues = []
for word, defn in defs2.items():
    invalid = []
    for ch in defn:
        if ord(ch) >= 0x4e00 and ord(ch) <= 0x9fff:
            if ch not in allowed_chars:
                if ch not in invalid:
                    invalid.append(ch)
    if invalid:
        issues.append((word, defn, invalid))

print(f'\n=== Definitions with INVALID chars: {len(issues)} ===')
for word, defn, invalid in sorted(issues):
    inv_info = []
    for ch in invalid:
        # check if this char was in a cut word
        sources = [w for w in all_cuts if ch in w]
        inv_info.append(f'{ch}(from:{",".join(sources[:3])})')
    print(f'  {word}: "{defn}" => bad: {" ".join(inv_info)}')

